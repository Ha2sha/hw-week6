package harshahw6;

public class Program19UpperToLower {
    public static void main(String[] args) {
        String s1 = "THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG.";//local variable
        String s2 = s1.toLowerCase();//converts to lowercase
        System.out.println("Output : " + s2);//the quick brown fox jumps over the lazy dog.
    }
}
